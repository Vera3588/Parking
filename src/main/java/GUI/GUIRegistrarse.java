    private void jBRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRegistrarseActionPerformed
        String bd_usuario = jTFusuarioRegistrarse.getText();
        String bd_clave = jTFcontrasenaRegistrarse.getText();  
        String bd_correo = jTFcorreoRegistrarse.getText();
        String comprobar="";
        
        Pattern pattern = Pattern
                .compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                        + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        
        String email = jTFcorreoRegistrarse.getText();

        Matcher mather = pattern.matcher(email);

        if (mather.find() == false) {
           JOptionPane.showMessageDialog(this, "Correo inv√°lido", "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else{
        
        conexion_bd conexionRegistroUsuarios = new conexion_bd();
        
        
        if(bd_usuario.isEmpty() || bd_clave.isEmpty() || bd_correo.isEmpty()){
            JOptionPane.showMessageDialog(this, "No hay datos suficientes. por favor complete las casillas.", "Error",
                    JOptionPane.ERROR_MESSAGE); 
        }
        
        else{
           
            try {
                Connection obtenerconexionBD = conexionRegistroUsuarios.getOracleConnection();
						
		PreparedStatement stmt =  obtenerconexionBD.prepareStatement("SELECT * FROM usuarios");
		//stmt.setString(1,bd_usuario);
			
		ResultSet rs= stmt.executeQuery();
						
		while(rs.next()){
		 			
		if(bd_usuario.equals(rs.getString("usuario"))){  
                    JOptionPane.showMessageDialog(this, "El usuario ya est√° registrado, intente con otro.", "Error",
                    JOptionPane.ERROR_MESSAGE);
                    comprobar = "Y";
		}
               }
                
                if(comprobar != ("Y")){
                 CreacionUsuarios nuevo = new CreacionUsuarios();
		 nuevo.RegistroUsuarios(bd_usuario, bd_clave, bd_correo);
                }
                
                rs.close();
                stmt.close();
                this.dispose();
            }
            
        catch (SQLException ex) {
            Logger.getLogger(GUIRegistrarse.class.getName()).log(Level.SEVERE, null, ex);
        }
     }
        }
        
    }//GEN-LAST:event_jBRegistrarseActionPerformed

